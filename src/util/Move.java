package util;

import java.io.Serializable;

public class Move implements Serializable{

	private static final long serialVersionUID = 7737996727014115946L;
	
	private int[] start;
	private int[] end;
	
	public Move(int[] start, int[] end){
		this.start = start;
		this.end = end;
	}
	
	public int[] start(){
		return start;
	}
	
	public int[] end(){
		return end;
	}
	
}
